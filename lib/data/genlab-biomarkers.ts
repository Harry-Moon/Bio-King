/**
 * Get full biomarker data for a gene symbol
 */
export function getBiomarker(geneSymbol: string): Biomarker | null {
  const def = BIOMARKER_DEFINITIONS[geneSymbol];
  if (!def) return null;
  return {
    geneSymbol,
    geneName: def.geneName,
    keggPathways: def.keggPathways,
  };
}

/**
 * Get all biomarkers for a health category
 */
export function getBiomarkersForCategory(category: string): Biomarker[] {
  const symbols = BIOMARKERS_BY_CATEGORY[category];
  if (!symbols) return [];

  const seen = new Set<string>();
  return symbols
    .filter((s) => {
      if (seen.has(s)) return false;
      seen.add(s);
      return true;
    })
    .map((s) => getBiomarker(s))
    .filter((b): b is Biomarker => b !== null);
}

/**
 * Get body system name from PDF category
 */
export function getSystemNameForCategory(category: string): string {
  return HEALTH_CATEGORY_TO_SYSTEM[category] ?? category;
}
